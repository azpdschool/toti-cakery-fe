// ==================== FUNGSI UNTUK AKSES DATA ====================

const delay = (ms: number = 300) => new Promise((resolve) => setTimeout(resolve, ms))

export async function getAllProducts(): Promise<Product[]> {
  await delay()
  return products
}

export async function getProductBySlug(slug: string): Promise<Product | undefined> {
  await delay()
  return products.find((p) => p.slug === slug)
}

export async function getCategories(): Promise<{ category: string; count: number }[]> {
  await delay()
  const map = new Map<string, number>()
  for (const product of products) {
    map.set(product.category, (map.get(product.category) || 0) + 1)
  }
  return Array.from(map.entries())
    .sort((a, b) => a[0].localeCompare(b[0]))
    .map(([category, count]) => ({ category, count }))
}

export async function getProductReviews(limit: number = 3): Promise<
  (ProductReview & { productId: string; productName: string })[]
> {
  await delay()
  const all = products.flatMap((product) =>
    product.reviews.map((review) => ({
      ...review,
      productId: product.id,
      productName: product.name,
    }))
  )
  return all.slice(0, limit)
}

export function formatRupiah(value: number): string {
  return new Intl.NumberFormat('id-ID', {
    style: 'currency',
    currency: 'IDR',
    maximumFractionDigits: 0,
  }).format(value)
}

export function getLowestPrice(variants: ProductVariant[]): number {
  return Math.min(...variants.map((v) => v.price))
}