// src/data/products.ts

export interface ProductVariantOptionGroup {
  key: string
  label: string
}

export interface ProductVariant {
  id: string
  name: string
  price: number
  options: Record<string, string>
  minOrder: number
  step: number
}

export interface ProductReview {
  id: string
  customerName: string
  rating: number
  comment: string
  purchasedProductName: string
}

export interface Product {
  id: string
  slug: string
  name: string
  category: string
  description: string
  image: string
  rating: number
  soldCount: number
  isFeatured: boolean
  optionGroups: ProductVariantOptionGroup[]
  variants: ProductVariant[]
  reviews: ProductReview[]
}

const placeholderImage = '/placeholder.png'

function makeVariant(
  id: string,
  name: string,
  price: number,
  options: Record<string, string> = {},
  minOrder = 1,
  step = 1
): ProductVariant {
  return { id, name, price, options, minOrder, step }
}

